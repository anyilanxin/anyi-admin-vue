    font-weight: 500;
    transition: all 0.5s;
    line-height: normal;
  }

  img {
    width: 32px;
  }
}
.anyi-layout-logo {
  background-color: v-bind(backCg);
  color: v-bind(color);
}
.anyi-layout-logo-dark {
  color: var(--color-neutral-10);
}

.anyi-layout-logo-center {
  justify-content: center;
  padding-left: 0px;
}
